package com.tsd.domain;

public class RateSalePolicy {

	//主键id
	private long id;
	
	//优惠策略名称
	private String saleName="";

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getSaleName() {
		return saleName;
	}

	public void setSaleName(String saleName) {
		this.saleName = saleName;
	}
}
